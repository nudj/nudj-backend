        <li><b><b>Cookies</b></b></li>
    </ul>
    <p>&nbsp;</p>

    <p>A cookie is a computer file consisting of letters and numbers which is installed on your device when you visit a
        mobile application (<b>App</b>) and which allow the App to recognise a user and retain that user’s preferences
        when
        they return. Cookies do not collect data, instead they serve to act as unique identifiers, or information
        caches,
        which can be used to identify a user’s preferences, settings or previous activity and which in turn enable App
        operators to provide an optimised, improved and more individualised browsing experience. For example, an App
        selling
        goods may remember the contents of a shopping basket, or a music App might recall songs you previously listened
        to.
    </p>

    <p>Cookies are typically ‘session’ or ‘persistent’ files. Session cookies are deleted when you leave an App or exit
        your
        browser, whereas persistent (also known as ‘tracking’) cookies remain stored on your device for a fixed period
        or
        until deleted by the user.</p>

    <p>Whilst we advise that you allow the installation of cookies on your browser or device when using the Services,
        you
        are not obliged to do so and you can choose to reject cookies by adjusting the settings for your browser or
        device.</p>

